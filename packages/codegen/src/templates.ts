function riskNotes(doc: StrategyDocument): string[] {
  switch (doc.templateId) {
    case "stop-loss":
      return [
        "Oracle price freshness and decimals must match the configured threshold.",
        "A stale or compromised oracle can make the order fill unexpectedly.",
      ];
    case "gas-guard":
      return [
        "Base fee behavior differs across some L2s.",
        "Strict gas caps may prevent otherwise profitable fills.",
      ];
    case "twap-slice":
      return [
        "TWAP fills depend on taker or keeper liveness.",
        "Partial fill accounting must be reviewed before mainnet use.",
      ];
    case "dca-schedule":
      return [
        "DCA execution depends on off-chain order publication and keeper behavior.",
        "Series metadata is informational and does not execute tranches itself.",
      ];
    default:
      return ["Review generated code and assumptions before use."];
  }
}
