export function generateTwapSliceGetter(doc: StrategyDocument): string {
  const b = doc.block;
  if (b.type !== "twap-slice") throw new Error("expected twap-slice block");
  return `// SPDX-License-Identifier: MIT
pragma solidity 0.8.23;

/// @title TwapSliceGetter — generated by Limit Canvas
/// @notice Caps per-fill making amount for TWAP-style partial fills
contract TwapSliceGetter {
    uint256 public immutable totalAmount;
    uint256 public immutable sliceAmount;
    uint256 public immutable intervalSeconds;
    uint256 public immutable startTime;

    constructor(uint256 _total, uint256 _slice, uint256 _interval, uint256 _start) {
        totalAmount = _total;
        sliceAmount = _slice;
        intervalSeconds = _interval;
        startTime = _start;
    }

    function maxMakingAmountNow() public view returns (uint256) {
        if (block.timestamp < startTime) return 0;
        uint256 elapsed = block.timestamp - startTime;
        uint256 slices = elapsed / intervalSeconds + 1;
        uint256 allowed = slices * sliceAmount;
        if (allowed > totalAmount) allowed = totalAmount;
        return allowed;
    }

    function getMakingAmount(
        uint256 requestedMaking,
        uint256 remainingMaking,
        bytes32 /* orderHash */
    ) external view returns (uint256) {
        uint256 cap = maxMakingAmountNow();
        uint256 maxFill = cap > remainingMaking ? remainingMaking : cap;
        return requestedMaking > maxFill ? maxFill : requestedMaking;
    }
}
`;
}

export function generateDcaRegistry(doc: StrategyDocument): string {
  const b = doc.block;
  if (b.type !== "dca-schedule") throw new Error("expected dca-schedule block");
  return `// SPDX-License-Identifier: MIT
pragma solidity 0.8.23;

/// @title DcaSeriesRegistry — generated by Limit Canvas
/// @notice On-chain metadata for DCA tranche series (orders signed off-chain)
contract DcaSeriesRegistry {
    event DcaSeriesRegistered(
        bytes32 indexed seriesKey,
        address indexed maker,
        uint256 tranches,
        uint256 amountPerTranche,
        uint256 intervalSeconds
    );

    struct Series {
        address maker;
        uint256 tranches;
        uint256 amountPerTranche;
        uint256 intervalSeconds;
    }

    mapping(bytes32 => Series) public series;

    function registerSeries(
        address maker,
        uint256 tranches,
        uint256 amountPerTranche,
        uint256 intervalSeconds,
        uint256 seriesId
    ) external returns (bytes32 key) {
        key = keccak256(abi.encode(maker, seriesId, block.chainid));
        series[key] = Series(maker, tranches, amountPerTranche, intervalSeconds);
        emit DcaSeriesRegistered(key, maker, tranches, amountPerTranche, intervalSeconds);
    }
}
`;
}

